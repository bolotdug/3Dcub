#include "libft.h"

void	ft_putendl_fd(char *s)
{
	int k;

	if (s != NULL)
	{
		k = 0;
		while (s[k] != '\0')
		{
			write(1, &s[k], 1);
			k++;
		}
		write(1, "\n", 1);
	}
}
